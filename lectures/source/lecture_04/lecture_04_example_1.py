day = input("Δώσε έναν αριθμό από 1-7: ").strip()

if day == "1":
    print("Κυριακή")
elif day == "2":
    print("Δευτέρα")
elif day == "3":
    print("Τρίτη")
elif day == "4":
    print("Τετάρτη")
elif day == "5":
    print("Πέμπτη")
elif day == "6":
    print("Παρασκευή")
elif day == "7":
    print("Σάββατο")
else:
    print("Λάθος αριθμός")
