# Υπολογισμός των πράξεων και εκτύπωση των αποτελεσμάτων τους

print(2 * (4 - 1)) # Αποτέλεσμα -> 6
print(2 * 4 - 1) # Αποτέλεσμα -> 7
print((2 + 1) ** (5 - 2)) # Αποτέλεσμα -> 27
print(2 ** 1 + 1) # Αποτέλεσμα -> 3
print(2 ** (1 + 1)) # Αποτέλεσμα -> 4

