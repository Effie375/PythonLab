# Διαβάζουμε από το χρήστη έναν αριθμό
num = int(input("Γράψε έναν ακέραιο αριθμό: ").strip())

# Όσο ο αριθμός είναι διάφορος του 0
while(num != 0):
    # Εκτύπωσε το διπλάσιο του
    print(2 * num)
    # Ξαναζήτα απο τον χρήστη για αριθμό
    num = int(input("Γράψε έναν ακέραιο αριθμό: ").strip())

